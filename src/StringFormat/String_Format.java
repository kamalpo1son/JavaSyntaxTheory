package src.StringFormat;
public class String_Format {
    public static void main(String[] args) {
        System.out.printf("%f This is %s a string, %d \n",10.4,"NICE",9);
        System.out.printf("String %-10d \n",532);
        System.out.printf("String %.2f \n",45.234544);
        System.out.printf("String %.2f \n",45.44840);
        System.out.printf("String %.2f \n",45.327410);
        System.out.printf("String %.2f \n",45.307483401803);
    }
}
