package src.IfAndElse;
public class If {
    public static void main(String [] args){
        int i=10;
        if(i<10){
            System.out.println("Less than 10");
        }
        else if(i>10){
            System.out.println("Higher than 10");
        }
        else {
            System.out.println("It's 10");
        }
    }
}
